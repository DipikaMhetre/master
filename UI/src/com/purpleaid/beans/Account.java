package com.purpleaid.beans;

public class Account {

	double accountId;
	
	String accountCode;
	
	String accountDescription;
	
	String accountType;
	
	boolean accountActiveFlag;

	public double getAccountId() {
		return accountId;
	}

	public void setAccountId(double accountId) {
		this.accountId = accountId;
	}

	public String getAccountCode() {
		return accountCode;
	}

	public void setAccountCode(String accountCode) {
		this.accountCode = accountCode;
	}

	public String getAccountDescription() {
		return accountDescription;
	}

	public void setAccountDescription(String accountDescription) {
		this.accountDescription = accountDescription;
	}

	public String getAccountType() {
		return accountType;
	}

	public void setAccountType(String accountType) {
		this.accountType = accountType;
	}

	public boolean isAccountActiveFlag() {
		return accountActiveFlag;
	}

	public void setAccountActiveFlag(boolean accountActiveFlag) {
		this.accountActiveFlag = accountActiveFlag;
	}
	
	
	
	
}
