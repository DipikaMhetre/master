package com.purpleaid.beans;

public class ImportantDates {
	
	double importantDatesId;
	
	double importantTypeId;
	
	String importantDates;
	
	String importantDatesTypesDescription;
	
	EntityDetails entity;
	
	boolean importantDatesIsDeleted;
	
	double importantDatesCDID;
	
	

	public boolean isImportantDatesIsDeleted() {
		return importantDatesIsDeleted;
	}

	public void setImportantDatesIsDeleted(boolean importantDatesIsDeleted) {
		this.importantDatesIsDeleted = importantDatesIsDeleted;
	}

	public double getImportantDatesCDID() {
		return importantDatesCDID;
	}

	public void setImportantDatesCDID(double importantDatesCDID) {
		this.importantDatesCDID = importantDatesCDID;
	}

	public EntityDetails getEntity() {
		return entity;
	}

	public void setEntity(EntityDetails entity) {
		this.entity = entity;
	}

	public String getImportantDatesTypesDescription() {
		return importantDatesTypesDescription;
	}

	public void setImportantDatesTypesDescription(
			String importantDatesTypesDescription) {
		this.importantDatesTypesDescription = importantDatesTypesDescription;
	}

	

	public double getImportantDatesId() {
		return importantDatesId;
	}

	public void setImportantDatesId(double importantDatesId) {
		this.importantDatesId = importantDatesId;
	}

	public double getImportantTypeId() {
		return importantTypeId;
	}

	public void setImportantTypeId(double importantTypeId) {
		this.importantTypeId = importantTypeId;
	}

	public String getImportantDates() {
		return importantDates;
	}

	public void setImportantDates(String importantDates) {
		this.importantDates = importantDates;
	}
	
	
	
	
}
