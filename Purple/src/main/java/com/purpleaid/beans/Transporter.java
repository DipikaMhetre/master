package com.purpleaid.beans;

public class Transporter {
	
	double transporterId;
	
	String transporterName;
	
	boolean transporterActiveFlag;

	public double getTransporterId() {
		return transporterId;
	}

	public void setTransporterId(double transporterId) {
		this.transporterId = transporterId;
	}

	public String getTransporterName() {
		return transporterName;
	}

	public void setTransporterName(String transporterName) {
		this.transporterName = transporterName;
	}

	public boolean isTransporterActiveFlag() {
		return transporterActiveFlag;
	}

	public void setTransporterActiveFlag(boolean transporterActiveFlag) {
		this.transporterActiveFlag = transporterActiveFlag;
	}
	

}
