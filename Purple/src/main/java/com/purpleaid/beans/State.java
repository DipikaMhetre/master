package com.purpleaid.beans;

public class State {
	
	String stateName;
	
	int stateId;
	
	boolean stateActiveFlag;

	public String getStateName() {
		return stateName;
	}

	public void setStateName(String stateName) {
		this.stateName = stateName;
	}

	public int getStateId() {
		return stateId;
	}

	public void setStateId(int stateId) {
		this.stateId = stateId;
	}

	public boolean isStateActiveFlag() {
		return stateActiveFlag;
	}

	public void setStateActiveFlag(boolean stateActiveFlag) {
		this.stateActiveFlag = stateActiveFlag;
	}
	
	
}
